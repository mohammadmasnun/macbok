// total Price-count
function totalPrice() {
    const fixedPrice = parseInt(document.getElementById("fixed-cost").innerText);
    const memoryPrice = parseInt(
        document.getElementById("memory-cost").innerText
    );
    const storagePrice = parseInt(
        document.getElementById("storage-cost").innerText
    );
    const DeliveryPrice = parseInt(
        document.getElementById("Delivery-cost").innerText
    );
    const total = fixedPrice + memoryPrice + storagePrice + DeliveryPrice;
    document.getElementById("total-cost").innerText = document.getElementById(
        "footer-total"
    ).innerText = total;
}

// promo code function
function promoApply() {
    const promoCode = document.getElementById("apply-input").value;
    if (promoCode == "stevekaku") {
        const totalValue = parseFloat(
            document.getElementById("total-cost").innerText
        );
        document.getElementById("footer-total").innerText =
            totalValue - totalValue * 0.2;
    }
    document.getElementById("apply-input").value = "";
}
